    private void obtenerSesionCliente() {
        I_ClienteRepository cr = new ClienteRepository(Connector.getConnection());
        
        cliente = cr.getById(Index.getSesionActual().getId());
        
        System.out.println(cliente);
    }    
    
    public void cargarTxt(){
        txtNombre.setText(cliente.getNombre());
        txtApellido.setText(cliente.getApellido());
        txtUsuario.setText(cliente.getUsuario());
    }
    
    public void cargarTabla(){
        I_EntradaRepository er = new EntradaRepository(Connector.getConnection());
        
        List<Entrada> listaEntradas = er.getByCliente(cliente);
        
        new Table<Entrada>().cargar(tblEntradas, listaEntradas);
    }

